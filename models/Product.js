import mongoose from "mongoose";

const ProductsSchema = new mongoose.Schema({
  id: {
    type: Number,
    required: true,
  },
  name: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  desc: {
    type: String,
    required: true,
  },
  category: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  isFev: {
    type: Boolean,
    required: true,
  },
});

mongoose.model("products", ProductsSchema);
